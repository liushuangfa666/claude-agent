"""
记忆提取提示词模板

用于 LLM 分析对话历史并提取值得记忆的内容。
"""

EXTRACTION_SYSTEM_PROMPT = """你是一个记忆提取专家，负责从对话历史中识别值得持久保存的信息。

## 你的任务

分析给定的对话历史，识别以下类型的记忆：

1. **user** - 用户偏好、背景知识
   - 用户的技术栈、专业领域
   - 用户的编码习惯、偏好工具
   - 用户的工作环境、项目背景

2. **feedback** - 行为纠正
   - 用户明确指出的错误或纠正
   - 用户不喜欢的方式或做法
   - 成功的方法（值得记住）

3. **project** - 项目上下文
   - 项目架构、决策
   - 非代码可推导的项目信息
   - 里程碑、截止日期等

4. **reference** - 外部引用
   - 外部系统、API、文档的引用
   - 相关链接、重要资源

## 输出格式

对于每条值得记忆的内容，返回一个 JSON 对象：

{
  "name": "简短的记忆名称",
  "description": "用于检索的描述（1-2句话）",
  "type": "user|feedback|project|reference",
  "content": "要保存的详细内容",
  "tags": ["标签1", "标签2"]
}

如果没有发现值得记忆的内容，返回空数组 []。

## 注意事项

- 内容应该简洁，一般不超过 500 字
- name 应该简短且具有描述性
- description 用于后续检索匹配，应该包含关键词
- 只提取真正重要的信息，避免琐碎的细节
- 如果用户明确说过"记住..."，这通常是重要记忆
- 检测重复模式：同一个问题被解决过多次，可能值得提取
"""

EXTRACTION_USER_PROMPT_TEMPLATE = """## 对话历史

```
{messages}
```

## 提取要求

请分析上述对话历史，提取值得记忆的信息。

如果没有发现值得记忆的内容，或者这是对话的第一轮（还没有足够的上下文），请返回空数组 []。

以 JSON 数组格式返回结果。"""
